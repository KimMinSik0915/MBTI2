package prototype;

public class MBTIVO {

	int ie, ns, tf, jp;

	public int getIe() {
		return ie;
	}

	public void setIe(int ie) {
		this.ie = ie;
	}

	public int getNs() {
		return ns;
	}

	public void setNs(int ns) {
		this.ns = ns;
	}

	public int getTf() {
		return tf;
	}

	public void setTf(int tf) {
		this.tf = tf;
	}

	public int getJp() {
		return jp;
	}

	public void setJp(int jp) {
		this.jp = jp;
	}

	@Override
	public String toString() {
		return "MBTIVO [ie=" + ie + ", ns=" + ns + ", tf=" + tf + ", jp=" + jp + "]";
	}
	
	
	
}
